package com.company;

class Author {
    String name;
    String birthPlace;

    public void setName(String _name) {
        name = _name;
    }

    public void setBirthPlace(String _birthPlace) {
        birthPlace = _birthPlace;
    }

    public String getName() {
        return name;
    }

    public String getBirthPlace() {
        return birthPlace;
    }
}
